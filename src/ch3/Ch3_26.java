package ch3;

public class Ch3_26 {
    public static void main(String[] args) {
        final double PI = 3.14159;                      //  設定具名常數PI和其值
        int r = 5;                                      //  圓半徑半徑
        System.out.println("圓周長 = " + 2 * PI * r);
        System.out.println("圓面積 = " + PI * r * r);
    }
}
