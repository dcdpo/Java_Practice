package ch3;

public class Ch3_25 {
    public static void main(String[] args) {
        final double PI;                                // 設定具名常數PI
        int r = 5;                                      // 圓半徑半徑
        PI = 3.14159;                                   // 實際設定PI值
        System.out.println("圓周長 = " + 2 * PI * r);
        System.out.println("圓面積 = " + PI * r * r);
    }
}
